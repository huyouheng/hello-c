                <p class="caption">这部分是你选了Materialize的Sass版本的说明。</p>
                <h4>编译 Sass</h4>
                <p>取而代之的有一个css文件夹, 你会发现这里包含很多以.scss为后缀的文件。这些文件包含了组件的样式。不幸的是，浏览器并不能解析Sass，所以你必须使用sass编译器去编译scss/materialize.scss文件并生成正常的css文件。做好这些你就可以将这个输出的文件引入到你的HTML页面中。</p>

                <pre class=" language-markup"><code class=" directory-markup language-markup">
  MyWebsite/
  |--css/
  |  |--materialize.css &lt;-- compiled from scss/materialize.scss
  |
  |--fonts/
  |  |--roboto/
  |
  |--js/
  |  |--materialize.js
  |
  |--scss/
  |  |--materialize.scss
  |  |--components/
  |
  |--index.html
